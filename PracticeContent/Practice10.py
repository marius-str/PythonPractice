# >>> For Loops <<<

names = ['Christopher', 'Susan']

for name in names:
    print(name)

print('######################')

for index in range(0, 2):
    print(index)
print('######################')

# >>> While Loops <<<

names2 = ['Christopher', 'Susan']
index = 0

while index < len(names2):
    name = names2[index]
    print(name)
    index = index + 1
